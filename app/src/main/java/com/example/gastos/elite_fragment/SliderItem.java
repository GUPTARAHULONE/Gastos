package com.example.gastos.elite_fragment;

public class SliderItem {
    String imageUrl,name;

    public SliderItem(String imageUrl, String name) {
        this.imageUrl = imageUrl;
        this.name = name;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getName() {
        return name;
    }
}
